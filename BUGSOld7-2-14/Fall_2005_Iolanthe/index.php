		<h2>Iolanthe (Fall 2005)</h2> 
		<p>Our first run of <em>Iolanthe</em> had three performances on November 18th and 19th and a gag show on November 20th, at List Art Center, Brown University.  Drawing from the best of both Gilbert and Sullivan at the height of their creative years, <em>Iolanthe</em> tells the tale of Iolanthe, a fairy, and Strephon, her son and an Arcadian shepherd, who wants to marry Phyllis, a Ward of Chancery. Phyllis' guardian, the Lord Chancellor, and half the House of Lords are sighing after her. Phyllis knows nothing of Strephon's fairy ancestry and when she sees Strephon kissing his youthfully beautiful mother, she assumes the worst and publicly rejects him, to the delight of her many suitors. The fairies interpose on behalf of Strephon, and soon the peers and the fairies are virtually at war, and long friendships are nearly torn asunder.  But all is happily sorted out, thanks to the "subtleties of the legal mind". </p>
