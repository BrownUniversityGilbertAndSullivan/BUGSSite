		<h2>Patter-a-Thon (Fall 2006)</h2> 
		<p>The Patter-a-Thon is an event unique to Brown University Gilbert and Sullivan modeled after fundraisers like Relay for Life. Anyone who wants to can volunteer to sing a patter song ("amusing lines delivered rapidly by an entertainer or performer, as in a comic routine or in a song," such as "I Am the Very Model of a Modern Major General," though not necessarily from G &amp; S in particular). We take turns one after another and see how long we can patter. The goal is to collect money based on the length of time we can sing continuously.</p>
