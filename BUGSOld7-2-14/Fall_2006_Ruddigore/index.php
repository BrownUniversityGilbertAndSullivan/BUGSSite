		<h2>Ruddigore (Fall 2006)</h2> 
		<p><em>Ruddigore</em> opened Friday, November 17th with two more performances on Saturday and a gag show Sunday afternoon. The tenth collaboration between Gilbert and Sullivan, <em>Ruddigore</em> (or <em>The Witch's Curse</em>) is a parody of the stock melodrama — with the moral absolutes of melodrama turned upside down: Good becomes bad, bad becomes good, and heroes take the easy way out. The Baronets of Ruddigore are cursed. Anyone who succeeds to the title has to commit a crime every day — or perish in inconceivable agony. Robin Oakapple, a young farmer loves Rose Maybud, but both are too shy to tell the other. But Robin has a secret. He is really Sir Ruthven Murgatroyd, the rightful Baronet of Ruddigore, in disguise. His younger brother, Despard, believing Ruthven to be dead, has assumed the title. Robin's foster brother, Richard, seeking Rose for himself, tells Despard of Robin's deception, and Robin is forced to accept his true position, losing Rose to Richard in the process. Now the Baronet of Ruddigore, Robin is confronted by the he ghosts of his ancestors who step from their picture frames in the gallery of Ruddigore Castle to confront him for failing to conscientiously commit his daily crime. Robin eventually finds a way of satisfying his ancestors demands whilst continuing to live a blameless life...</p>
