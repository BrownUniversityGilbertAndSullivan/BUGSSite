		<h2>Yeomen of the Guard (Fall 2007)</h2> 
		<p><em>Yeomen of the Guard</em> opened on Friday, November 16th at Alumnae Hall with two more performances on Saturday and a gag show on Sunday. The darkest of the Gilbert and Sullivan operas, Yeomen ends with a broken-hearted main character and at least two reluctant engagements, rather than the usual armful of marriages. However, Gilbert's "pointed" satire and punning one-liners abound, there are plenty of topsy-turvy plot complications, and many believe that the score is Sullivan's finest. Indeed, some enjoy Yeomen particularly because of its ever-changing emotional balance of joy and despair, love and sacrifice. The setting of Yeomen is laid in the Tower of London in Shakespearean times. The plot concerns Colonel Fairfax, a gentleman, soldier and scientist, who has been sentenced to be beheaded in an hour on a false charge of sorcery. To avoid leaving his estate to his accuser (a cousin), and with the help of the Lieutenant of the Tower, Fairfax secretly marries Elsie Maynard, a strolling singer. The bride agrees to be blindfolded during the ceremony and expects to be a well-paid widow in an hour. With the help of the Meryll family, Fairfax escapes, throwing the Tower into confusion and the astonished Elsie (and her mentor, the jester Jack Point, who loves her) into despair. But Fairfax, disguised as Leonard Meryll, woos Elsie, and after a number of plot complications are worked out, she falls in love with Fairfax and leaves Jack Point broken-hearted.</p>
