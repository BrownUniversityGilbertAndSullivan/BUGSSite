		<h2>Patience (Fall 2008)</h2> 
		<p><em>Patience</em> opened on Friday, November 21st at Alumnae Hall, Brown University with two more performances the following Saturday and a gag show on Sunday.  The sixth of Gilbert's and Sullivan's collaborations, Patience satirizes the "aesthetic craze" of the 1870's and '80s, when the output of poets, composers, painters and designers of all kinds was indeed prolific — but, some argued, empty and self-indulgent. This artistic movement was so popular, and also so easy to ridicule as a meaningless fad, that it made Patience a big hit. In the story, all the well-born young ladies in the village are rapturously caught up in aestheticism, and have deserted their military suitors for the love of two contrasting aesthetic poets — a "fleshy" poet and an "idyllic" poet. The poets, however, are both in love with Patience, the simple village milkmaid, who cares nothing for poetry. Patience learns that true love must be completely unselfish--it must wither and sting and burn! As for the military men, they don't see the point to aesthetics, but they decide to give it a try to win the women's hearts. It is touch and go for awhile, but everyone ends up with a suitable partner, even if it is only a tulip or lily. </p>
