		<h2>The Gondoliers (Fall 2009)</h2>
		<p>Our production of <em>The Gondoliers</em> opened on November 20th, running for four performances that weekend. In <em>Gondoliers</em>, the last of the great Gilbert and Sullivan successes, the Duke and Duchess of Plaza-Toro arrive in Venice and are seeking the heir to the throne of Barataria, to whom their daughter Casilda was married in infancy. Casilda has secretly been in love with the Duke's attendant, but the lovers resign themselves to the fact that they can never be together. The Grand Inquisitor informs the ducal family that Casilda's missing husband is in fact one of two men brought up as gondoliers, but no-one knows precisely which of the two is the King except their foster mother, presently at large. Unfortunately, not only do the gondoliers have their own ideas of government - their idealistic egalitarianism is hardly appreciated by the Grand Inquisitor and the Duke and Duchess - but they themselves have just gotten married and are much distraught to learn that one of them is an unintentional bigamist. Only after everyone spends much time unnecessary wrangling over the details of the situation, the gondoliers' foster mother emerges from the torture chamber to reveal the truth and everyone and everything are happily sorted out.</p>
