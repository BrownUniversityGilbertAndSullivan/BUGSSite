		<p>Our production of <em>Ruddigore</em> opened on November 19th, running for four performances that weekend. <em>Ruddigore</em> opens with a chorus of professional bridesmaids worrying because there have been no weddings for months in the village. All the eligible young men are in love with Rose Maybud, but are too timid to approach her. The desperate bridesmaids ask Dame Hannah if she would consider marrying, but she reveals that she is sworn to an eternal maidenhood. She reveals the curse upon the Murgatroyd line, which dooms all Baronets of Ruddigore to commit a crime every day, or perish in inconceivable agony. <br><BR>Robin Oakapple, the current Baronet who has disguised himself to hide from the curse, and Rose are both in love with each other, but are too shy to express their feelings. Robin's foster-brother Richard Dauntless arrives and offers to speak to Rose on Robin's behalf, but falls in love with her himself and proposes to her. When Robin finds out, he immediately declares his own love for Rose, who then changes her mind and accepts Robin. Richard finds Robin's younger brother Despard, who has assumed the curse, and reveals that Robin is in fact alive and in disguise. On the day of the wedding, Despard publicly accuses Robin of being a baronet of Ruddigore, which Robin grudgingly acknowledges is true. Robin takes up the mantle of baronet, and Rose is forced to choose Richard, "the only one that's left. <br><br> Robin is unsuccessful as a bad baronet, not impressing his ancestors at all. The ghosts of his ancestors make him swear to carry off a lady lest he perish in terrible agony. Despard and his new wife, Margaret, urge Robin to renounce his life of crime and face his ancestors. Through some logical leaps, Robin manages to escape his fate and marry Rose, everyone finds a partner, and everyone ends up happy. </p>
