		<p>Our first ever full scale production, <em>The Mikado</em> ran in April of 2005. The single most popular of all Gilbert &amp; Sullivan shows, it follows the misadventures of Nanki-Poo, the disguised son of the Mikado (Emperor of Japan), as he flees from the advances of the elderly court lady Katisha and searches for his true love, Yum-Yum.  Unfortunately, he must deal with a rival in Yum-Yum's own guardian Ko-Ko, the Lord High Executioner.  As Ko-Ko is under Imperial pressure to find someone to execute soon or be found remiss in his duties, Nanki-Poo offers himself up on the stipulation that he be permitted to marry Yum-Yum for the brief time that he remains alive, preferring to face death than life without her.  The tender-hearted Ko-Ko, ultimately unable to bring himself to carry out the execution, agrees instead to forge the death certificate and let Nanki-Poo go into hiding - only to face the threat of a most hideous death for the alleged execution of the Heir to the Throne of Japan.  Eventually, of course, all is resolved and everyone winds up alive and happy.  "Nothing," says the Mikado, "could possibly be more satisfactory."</p>
