		<h2>The Pirates of Penzance (Spring 2006)</h2> 
		<p><em>The Pirates of Penzance</em> had one performance Friday, March 17th, two more performances the 18th, and a gag show on the 19th. Following close on the heels of Gilbert's and Sullivan's first blockbuster hit <em>H.M.S. Pinafore</em>, <em>Pirates</em> remains one of the best-loved and most-performed G &amp; S shows. In the opera, we follow the misadventures of Frederic, who as a child had been apprenticed to a band of tenderhearted, orphaned pirates through an unfortunate misunderstanding on the part of his nurse. Frederic, upon completing his 21st year, rejoices that he has fulfilled his indentures and is now free to return to respectable society. But he soon discovers that he was born on February 29 in leap year, and he remains apprenticed to the pirates until his 21st birthday, which will not be until he reaches the ripe old age of eighty-four. By the end of the opera, the pirates, a Major General who knows nothing of military strategy, his large family of beautiful but unwed daugters, and the timid constabulary all contribute to a cacophony that can be silenced only by Queen Victoria's name. </p>
