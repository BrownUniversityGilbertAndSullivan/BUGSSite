		<h2>Our 4th Annual Sing-In (Spring 2009)</h2> 
		<p>A long-standing G &amp; S tradition, sing-ins are community gatherings where Savoyards sing through works from the canon with piano accompaniment.  There is no dialog and little or no movement (although people are certainly welcome to act if they wish).  Everyone sings the choral parts, often without regard to gender, while leads are cast in advance.  One of the chief goals of the sing-in is to be as fun and stress free as possible - it is an excellent opportunity to dust off a role you've always loved or learn a new part that might not fit your usual archetype.</p>
		<p>Held on April 25th in the Steinert Hall Choral Room, the menu for our fourth sing-in included Ruddigore, the Act I finale of Pinafore, Iolanthe, Gondoliers, Act III of Ida, and Pirates.  As always, coffee, bagels, and pizza were also served.</p>
		
			
