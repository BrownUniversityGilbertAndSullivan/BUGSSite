		<h2>Iolanthe (Spring 2009)</h2> 
		<p>Our second production of <em>Iolanthe</em> opened on April 3rd at Alumnae Hall, Brown University with four successive perfomances over the weekend.  Drawing from the best of both Gilbert and Sullivan at the height of their creative years, <em>Iolanthe</em> tells the tale of Iolanthe, a fairy, and Strephon, her son and an Arcadian shepherd, who wants to marry Phyllis, a Ward of Chancery. Phyllis' guardian, the Lord Chancellor, and half the House of Lords are sighing after her. Phyllis knows nothing of Strephon's fairy ancestry and when she sees Strephon kissing his youthfully beautiful mother, she assumes the worst and publicly rejects him, to the delight of her many suitors. The fairies interpose on behalf of Strephon, and soon the peers and the fairies are virtually at war, and long friendships are nearly torn asunder.  But all is happily sorted out, thanks to the "subtleties of the legal mind". </p>
