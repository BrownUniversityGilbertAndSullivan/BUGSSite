		<p>Our production of Princess Ida opened in April 2011 and ran for four performances. The only show by Gilbert and Sullivan with three acts, it is considered to have one of the pair's best scores.</p>

<p>Princess Ida opens at the palace of King Hildebrand with courtiers awaiting the arrival of King Gama, whose daughter, Ida, is betrothed to Hildebrand's son Hilarion. In the 20 years since the betrothal was made when the two were babies, Hilarion has not seen Ida. Nevertheless, he eagerly awaits meeting her. King Gama and his three warlike sons arrive and reveal that Ida has sworn off men. Hilarion and his friends Florian and Cyril suspect they can charm Ida into fulfilling the betrothal, but Hildebrand threatens Gama unless Ida marries Hilarion.</p>

<p>Meanwhile, Ida is the head of Castle Adamant, a women's college. The girls there and their teachers, Lady Blanche and Lady Psyche, adore learning, and Ida is particularly confident about her skills. Hilarion, Florian, and Cyril sneak into the school and disguise themselves as women to get closer to Ida. Lady Psyche, Florian's sister, learns of their deception, as does Melissa, who falls in love with Florian. They try to hide the trio's identity until the drunken Cyril reveals it. An outrage Ida falls into the water but is saved by Hilarion, who makes a plea of love to her. Soon after, Hildebrand's men storm the castle. Ida, who had almost been swayed, rallies the women to defend against the men.</p>

<p>The women appear to be prepared to fiercely defend Ida and their school, but Ida soon discovers that their dedication is hollow. She laments the fact that she will be alone in the fight. Ida's three brothers then arrive to defend her, and they fight Hilarion, Florian, and Cyril. The latter trio wins, and Ida admits her love for Hilarion, though she still has ambitions for power.</p>
