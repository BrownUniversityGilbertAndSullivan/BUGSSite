		<p>The one-act <em>Trial By Jury</em>, BUGS' first-ever summer production, had two performances on July 12 and 13 at the List Art Building, Brown University.  The first surviving opera to be produced by Gilbert and Sullivan, it laid the groundwork for much of their successful future collaboration.  The story, a trial of an errant lover, seems simple enough; but soon, as we descend into the moral twilight, we find that nobody is truly above reproach.</p>
